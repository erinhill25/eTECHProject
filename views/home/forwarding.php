	<h1>Set up Email Forwarding</h1>

	<p>This is a brief tutorial on how to set up email forwarding on your accounts, provided they all use office 365. Most likely, you have several email accounts in your posession and you'd like to consolodate the incoming messages into one preferred email.</p>

	<ol class='forwarding'>
		<li>
			<h2>Login to one of your email accounts you'd like to forward from</h2>
			<p>You may find the login location at the campus website where the email is from. You'll find a helpful list of locations at the <a href="https://360etech.org/app/answers/detail/a_id/2572" target="_blank">eTECH 360 Website</a></p>
		</li>
		<li>
			<h2>Click on the gear icon and select "Options"</h2>
			<p>The gear will be in the upper right hand corner of outlook 365</p>
			<img src="<?php echo BUCKET; ?>/forwarding1.png" alt="Choose the gear icon" class='imageHelper' />
		</li>
		<li>
			<h2>Under "Accounts" choose the option "Forwarding"</h2>
			<p>This option is on the left side of the page</p>
			<img src="<?php echo BUCKET; ?>/forwarding2.png" alt="Select Forwarding" class='imageHelper' />
		</li>
		<li>
			<h2>Select "Start Forwarding" and enter your preferred Email Address</h2>
			<p>It is recommended that you keep a copy of the sent emails</p>
			<img src="<?php echo BUCKET; ?>/forwarding3.png" alt="Enter your preferred Email Address" class='imageHelper' />
		</li>
		<li>
			<h2>Hit Save at the top and repeat these steps for all the email accounts you wish to forward</h2>
			<p>Afterwards, communication will be sent to this one address. The 360 eTECH portal alerts will also be sent there, provided it is the preferred email set in your profile. </p>
		</li>
	</ol>

</div>	